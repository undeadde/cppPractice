#include <iostream>
using namespace std;
int main(int argc, char *argv[])
{
    int a = 20, b = 20;
    std::cout << " a = " << a << ", b = " << b << std::endl;
    std::cout << "a++ = " << a++ << ", ++b = " << ++b << std::endl;
    std::cout << " a = " << a << ", b = " << b << std::endl;
    return 0;
}
